import logging
import time
import random

class DialogAgent:
    def __init__(self):
        logging.debug("--> Диалоговый агент инициализирован")
        self.responses = [
            "Привет! Я ваш AI ассистент. Чем могу помочь?",
            "Отличный вопрос! Давайте разберемся вместе.",
            "На основе моих данных, я могу предложить следующее...",
            "Это интересная тема! Что именно вас интересует?",
            "Я обрабатываю ваш запрос и скоро дам подробный ответ.",
            "Для более точного ответа мне нужно уточнить некоторые детали.",
            "Согласно имеющейся информации, могу сказать следующее...",
            "Давайте углубимся в эту тему. Что вы хотите узнать?",
            "Я нашел несколько релевантных фактов по вашему вопросу.",
            "Отличная беседа! Есть ли еще что-то, что вас интересует?"
        ]
    
    def say(self, message):
        """Основной метод для получения ответа от ассистента"""
        # Имитация обработки запроса
        time.sleep(1)
        
        # Специальные ответы для определенных запросов
        message_lower = message.lower()
        
        if "привет" in message_lower:
            return "Привет! Рад вас видеть! Как ваши дела?"
        elif "как дела" in message_lower:
            return "У меня всё отлично! Готов помочь вам с любыми вопросами."
        elif "спасибо" in message_lower:
            return "Пожалуйста! Обращайтесь еще!"
        elif "пока" in message_lower or "до свидания" in message_lower:
            return "До свидания! Хорошего дня!"
        elif "помощь" in message_lower:
            return "Я могу ответить на ваши вопросы, помочь с информацией или просто пообщаться!"
        elif "статус" in message_lower:
            return "✅ Агент работает в штатном режиме. Все системы функционируют нормально."
        elif "кто ты" in message_lower:
            return "Я AI ассистент, созданный для помощи пользователям. Чем могу быть полезен?"
        
        # Случайный ответ из списка для обычных запросов
        return random.choice(self.responses)